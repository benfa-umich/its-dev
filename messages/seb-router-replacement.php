<h1>Core Router Replacement at SEB on 2/15/14</h1>

             <p>The core network router at the School of Education Building (SEB) will be replaced on Saturday, February 15. The work will begin at 5 a.m. and is expected to complete by 5 p.m.</p> 

						<h2>Steps to Mitigate Possible Campus Risks</h2>
						
						<ul>
						<li>The Saturday 5 a.m. to 5 p.m. time was chosen since it is a less critical time in terms of network use, minimizing impact in case of loss of campus network connectivity.</li>
						<li>The <a href="http://its.umich.edu/help/">ITS Service Center's</a> regular staffing will be augmented by UMNet Operations personnel to answer questions and provide technical support to the U-M community.
							<ul>
							<li>A conference line with a team of experts will remain open to provide support to the Service Center staff.</li>
							</ul>
						</li>
						<li>The <a href="http://status.its.umich.edu/">Service Status page</a> on the ITS website will be regularly updated as the work progresses.</li>
						<li>Work will be performed in the following order:
							<ol>
							<li>Installation of new core router</li>
							<li>Data center circuits activated</li>
							<li>Links between core routers enabled</li>
							<li>Buildings attached to the core network are brought online</li>
							</ol>
						</li>
						</ul>

						<p>However, unanticipated issues can arise due to new combinations of events and processes. With due diligence and application of lessons learned from the previous three router replacements, the project team is confident of dealing with obstacles in a timely manner.</p>

						<h2>Core Router Replacement Project Status</h2>
						
						<p>Three out of the four core routers on campus have been replaced in recent months. The earlier stages of the project experienced some delays due to technical difficulties, and the timing of critical university events and business processes. The technical issues have since been resolved and plans are in place to complete the process by this weekend.</p>

						<h2>The 100 GB Innovation Network Project</h2>

						<p>The replacement of the four core routers will enable the university to make larger infrastructure improvements in the future. As one of the <a href="http://cio.umich.edu/initiatives/">FY14 University IT Priorities</a>, the 100 GB Innovation Network project will significantly upgrade the university's capability to provide cutting-edge speed and facilitate large scale data transmission.</p>

						<p>This will be accomplished in the following four stages:</p>

						<ol>
						<li>Upgrading the core network to 100Gbps capacity</li>
						<li>Connecting the data centers (MACC and MDC) to the core network at 100Gbps</li>
						<li>Creating a 100Gbps connection to Internet2</li>
						<li>Attaching 30 new buildings to the core network at 10Gbps</li>
						</ol>
						
						<p>Once the core routers are replaced, work will proceed to the subsequent stages of this project.</p>

